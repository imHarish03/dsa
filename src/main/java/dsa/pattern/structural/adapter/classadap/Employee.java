package dsa.pattern.structural.adapter.classadap;

public class Employee {

	private String fullName;
	private String jobTitle;
	private String officeLocarion;

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}

	public void setOfficeLocarion(String officeLocarion) {
		this.officeLocarion = officeLocarion;
	}

	public String getFullName() {
		return fullName;
	}

	public String getJobTitle() {
		return jobTitle;
	}

	public String getOfficeLocarion() {
		return officeLocarion;
	}



}
